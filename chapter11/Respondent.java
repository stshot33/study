package chapter11;

// 설문 응답자의 지역과 나이를 저장하는 클래스
public class Respondent {
	private int age;
	private String location;

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

}
