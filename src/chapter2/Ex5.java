package chapter2;

public class Ex5 {
	public static void main(String[] args) {
		// double -> 8Byte
		// float -> 4Byte
		
		// float 타입의 변수에 실수를 저장할 때는 항상 끝에 f를 붙어야함
		float var1 = 0.1234567890123456789f;
		
		double var2 = 0.1234567890123456789;
		
		System.out.println("var1 = " + var1);
		System.out.println("var2 = " + var2);
	}
}


