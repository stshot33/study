package chapter2;

public class Ex13 {
	public static void main(String[] args) {
		double num1 = 3.14;
		int num2 = 69;
		
		// 실수를 정수로 변환
		int num3 = (int) num1;
		
		// 정수를 문자로 변환
		char ch = (char) num2;
		
		System.out.println("double -> int / num3 = " + num3);
		System.out.println("int -> char / ch = " + ch);
	}
}




