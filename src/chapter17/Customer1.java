package chapter17;

public class Customer1 {
	private String tel;
	private String pay;
	
	public Customer1(String tel, String pay) {
		this.tel = tel;
		this.pay = pay;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getPay() {
		return pay;
	}

	public void setPay(String pay) {
		this.pay = pay;
	}

}
