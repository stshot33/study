package chapter6;

public class Ex10 {
	public static void main(String[] args) {
		int num = 1;
		
		System.out.println(num);
		
		System.out.println("<< 반복문 시작 >>");
		while(num <= 10) {
			System.out.println(num);
			
			num++;
		}
		System.out.println("<< 반복문 종료 >>");
		
		System.out.println(num);
	}
}
