package chapter5;

public class Ex19 {
	public static void main(String[] args) {
		int num1 = 2;
		
		switch(num1) {
			case 1:
				int num2 = 1;
				System.out.println("num2 = " + num2);
				break;
			case 2:
				num2 = 2;
				System.out.println("num2 = " + num2);
				break;
			case 3:
				num2 = 3;
				System.out.println("num2 = " + num2);
				break;
		}
		
		System.out.println("num1 = " + num1);
	}
}
