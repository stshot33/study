package chapter3;

public class Ex6 {
	public static void main(String[] args) {
		char ch1 = 'a';	// 97
		char ch2 = 'A'; // 65
		
		System.out.println(ch1 == ch2);
		
		System.out.println(ch1 != ch2);
		
		System.out.println(ch1 > ch2);
		
		System.out.println(ch1 >= ch2);
		
		System.out.println(ch1 < ch2);
		
		System.out.println(ch1 <= ch2);
	}
}
